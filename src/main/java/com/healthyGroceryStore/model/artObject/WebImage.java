package com.healthyGroceryStore.model.artObject;

public class WebImage {
  private int offsetPercentageY;
  private int offsetPercentageX;
  private int width;
  private String guid;
  private String url;
  private int height;

  public int getOffsetPercentageY() {
    return offsetPercentageY;
  }

  public int getOffsetPercentageX() {
    return offsetPercentageX;
  }

  public int getWidth() {
    return width;
  }

  public String getGuid() {
    return guid;
  }

  public String getUrl() {
    return url;
  }

  public int getHeight() {
    return height;
  }
}
